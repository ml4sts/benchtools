import os 
import logging 

log_folder: str

def init_logger(log_path, task_name):
    ''''
    Initializes the logger for a specific task.
    
    Parameters:
    -------------
    log_path: str
        The path to the log folder where the log file will be created.
    task_name: str
        The name of the task for which the logger is being initialized. This will be used to name the log file.
    '''
    log_file = os.path.join(log_path, f'{task_name}_log.txt')
    logging.basicConfig(filename=log_file, 
                    level=logging.INFO, format= '%(asctime)s - %(levelname)s - %(message)s')

def log_agent_interaction(agent_input, agent_output):
    """
    Logs the agent's input and output to a file.

    Parameters:
    -------------
    agent_input: string
        The input provided to the agent.
    agent_output: string
        The output generated by the agent.
    """
    logging.info(f'Input: {agent_input}')
    logging.info(f'Output: {agent_output}')



    

